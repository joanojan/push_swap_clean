int	arr_is_sorted(int *arr, int len)
{
	int	i;

	if (!arr || len < 2)
		return (1);
	i = 1;
	while (i < len)
	{
		if (arr[i - 1] > arr[i])
			return (0);
		i++;
	}
	return (1);
}
